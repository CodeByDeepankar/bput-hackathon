function normalizeBranchKey(branch) {
  if (!branch) return null;
  return String(branch).trim().toUpperCase();
}

function toDisplayBranch(branch) {
  if (!branch) return null;
  const trimmed = String(branch).trim();
  if (trimmed.length <= 3) return trimmed.toUpperCase();
  return trimmed.charAt(0).toUpperCase() + trimmed.slice(1).toLowerCase();
}

export function getSubjectsForBranch(branch, semester) {
  const normalizedBranch = normalizeBranchKey(branch);
  const semesterNumber = Number(semester);
  if (!normalizedBranch || !Number.isFinite(semesterNumber)) return [];
  const branchBucket = BRANCH_SUBJECTS[normalizedBranch];
  if (!branchBucket) return [];
  return branchBucket[semesterNumber] || [];
}

export function formatBranchName(branch) {
  return toDisplayBranch(branch) || "CSE";
}
